int celsiusToFahrenheit(int value) => (value * 1.8 + 32).round();

int fahrenheitToCelsius(int value) => ((value - 32) / 1.8).round();

int kgToPounds(int value) => (value * 2.2).round();

int poundsToKg(int value) => (value / 2.2).round();

int millilitersToOunces(int value) => (value / 29.5735).round();

int ouncesToMilliliters(int value) => (value * 29.5735).round();
